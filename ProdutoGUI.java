    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // Salvar

        Produto u = new Produto();

        //Validacoes
        if (txtPrecoCusto.getText().trim().equals("") || txtPrecoCusto.getText().trim().equals("")
            || txtPrecoVenda.getText().trim().equals("")
            || txtDescricao.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Nenhum campo pode estar vazio!");
            return;
        }

        u.setPrecoCusto(txtPrecoCusto.getText());
        u.setPrecoVenda(txtPrecoVenda.getText());
        u.setDescricao(txtDescricao.getText());
        new ProdutoDAO().salvar(u);
        JOptionPane.showMessageDialog(this, "Cadastrado com Sucesso!");

        listProduto = new ProdutoDAO().listarTodos();

        adicionarTable();

    }//GEN-LAST:event_btnSalvarActionPerformed

