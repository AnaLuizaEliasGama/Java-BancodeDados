    private void adicionarTable() {
        DefaultTableModel model = (DefaultTableModel) table.getModel();

        model.setNumRows(0);

        for (Produto u : listProduto) {
            model.addRow(new Object[]{
                u.getId(),
                u.getDescricao(),
                u.getPrecoCusto(),
                u.getPrecoVenda()
            });
        }

        table.setModel(model);

    }

