    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
        if (table.getSelectedRow() > -1) {

            int idSelecionado = Integer.parseInt(listProduto.get(table.getSelectedRow()).getId());

            new ProdutoDAO().deletar(idSelecionado);
            JOptionPane.showMessageDialog(this, "Deletado Com Sucesso!");
            listProduto = new ProdutoDAO().listarTodos();

            adicionarTable();

        }
    }//GEN-LAST:event_btnDeletarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        if (txtPrecoCusto.getText().trim().equals("") && txtDescricao.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Nenhum produto encontrado!");
            return;
        }

        if (txtPrecoCusto.getText().equals("")) {
            String descricao = txtDescricao.getText();
            listProduto = new ProdutoDAO().buscar(descricao, "descricao");
            if (listProduto.size() > 0) {
                adicionarTable();
                return;
            } else {
                JOptionPane.showMessageDialog(this, "Nenhum produto encontrado!");
            }
        }

        if (txtDescricao.getText().equals("")) {
            String descricao = txtDescricao.getText();
            listProduto = new ProdutoDAO().buscar(descricao, "descricao");
            if (listProduto.size() > 0) {
                adicionarTable();
                return;
            } else {
                JOptionPane.showMessageDialog(this, "Nenhum produto encontrado!");
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

